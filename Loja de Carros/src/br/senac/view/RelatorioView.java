public String dataFormato(String data) throws ParseException {

        SimpleDateFormat formatadorDatas = new SimpleDateFormat("dd/MM/yyyy");
        Date d = formatadorDatas.parse(data);
        formatadorDatas = new SimpleDateFormat("yyyy-MM-dd");
        String novaData = formatadorDatas.format(d);
        return novaData;
    }
