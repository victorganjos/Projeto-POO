    public void loadTableRealatorios(String dataInicio, String dataFim) throws ParseException {
        ArrayList<String[]> listaRelatorio = RelatorioController.read(dataInicio, dataFim);

        DefaultTableModel tbRelatorio = new DefaultTableModel();

        tbRelatorio.addColumn("ID VENDA");
        tbRelatorio.addColumn("Cliente");
        tbRelatorio.addColumn("Valor Total");
        tbl_TabelaRelatorio.setModel(tbRelatorio);

        for (String[] c : listaRelatorio) {
            tbRelatorio.addRow(c);
        }

        tbl_TabelaRelatorio.getColumnModel().getColumn(0).setPreferredWidth(80);
        tbl_TabelaRelatorio.getColumnModel().getColumn(1).setPreferredWidth(80);
        tbl_TabelaRelatorio.getColumnModel().getColumn(2).setPreferredWidth(100);

    }
    
    public void loadTableRelatorio(int id) {

        ArrayList<String[]> listaVendas = ProdutoController.read2(id);

        DefaultTableModel tbItemVenda = new DefaultTableModel();

        tbItemVenda.addColumn("ID Produto");
        tbItemVenda.addColumn("Modelo");
        tbItemVenda.addColumn("Valor Unit.");
        tbl_TabelaRelatorioItensVenda.setModel(tbItemVenda);

        for (String[] c : listaVendas) {
            tbItemVenda.addRow(c);
        }

        tbl_TabelaRelatorioItensVenda.getColumnModel().getColumn(0).setPreferredWidth(80); //ID
        tbl_TabelaRelatorioItensVenda.getColumnModel().getColumn(1).setPreferredWidth(80); //ID
        tbl_TabelaRelatorioItensVenda.getColumnModel().getColumn(2).setPreferredWidth(100);

    }

    private void valoresRelatorio() {
        double auxValorTotal = 0.0;
        for (int i = 0; i < tbl_TabelaRelatorio.getRowCount(); i++) {
            auxValorTotal += Double.parseDouble(tbl_TabelaRelatorio.getValueAt(i, 2).toString());
        }
        txt_ValorTotalItensPeriodo.setText(String.valueOf(auxValorTotal));

    }

